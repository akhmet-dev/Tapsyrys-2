export const getStoredLanguage = () => {
  try {
    const saved = localStorage.getItem('lang');
    if (saved === 'ru' || saved === 'kk') {
      return saved;
    }

    const browserLanguage = navigator.language?.slice(0, 2);
    return browserLanguage === 'ru' ? 'ru' : DEFAULT_LANGUAGE;
  } catch {
    return DEFAULT_LANGUAGE;
  }
};

export const getCommonMessage = (language, key) =>
  COMMON_MESSAGES[language]?.[key] ?? COMMON_MESSAGES[DEFAULT_LANGUAGE][key] ?? key;

export const getLocalizedValue = (language, group, value) =>
  DOMAIN_MESSAGES[language]?.[group]?.[value] ??
  DOMAIN_MESSAGES[DEFAULT_LANGUAGE]?.[group]?.[value] ??
  value;

export const getDateLocale = (language) => (language === 'ru' ? 'ru-RU' : 'kk-KZ');

export const formatLocalizedDate = (dateValue, language) =>
  new Date(dateValue).toLocaleDateString(getDateLocale(language));

export const formatLocalizedDateTime = (dateValue, language) =>
  new Date(dateValue).toLocaleString(getDateLocale(language), {
    year: 'numeric',
    month: '2-digit',
    day: '2-digit',
    hour: '2-digit',
    minute: '2-digit',
  });

export const localizeBackendMessage = (message, language = DEFAULT_LANGUAGE) => {
  if (!message || language !== 'ru') {
    return message;
  }

  const exactMatch = BACKEND_MESSAGES_RU[message];
  if (exactMatch) {
    return exactMatch;
  }

  const duplicateSpecialityMatch = message.match(/^Сіз "(.+)" мамандығына бұрыннан өтінім бергенсіз\.$/);
  if (duplicateSpecialityMatch) {
    const speciality = getLocalizedValue(language, 'specialities', duplicateSpecialityMatch[1]);
    return `Вы уже подавали заявку на специальность "${speciality}".`;
  }

  const statusChangedMatch = message.match(/^Өтінім статусы "(.+)" болып өзгертілді\.$/);
  if (statusChangedMatch) {
    const status = getLocalizedValue(language, 'statuses', statusChangedMatch[1]);
    return `Статус заявки изменен на "${status}".`;
  }

  const requiredDocumentMatch = message.match(/^(.+) құжатын жүктеу міндетті\.$/);
  if (requiredDocumentMatch) {
    return `Необходимо загрузить документ: "${requiredDocumentMatch[1]}".`;
  }

  return message;
};
